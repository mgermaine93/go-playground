// Use a variety of operators to write expressions and assign their values to variables.  Then, print each of the variables.

package main

import "fmt"

func main() {

	// ==
	a := 14 == 14

	// <=
	b := 14 <= 14

	// >=
	c := 14 >= 14

	// !=
	d := 14 != 14

	// <
	e := 14 < 14

	// >
	f := 14 > 14

	fmt.Println(a) // true
	fmt.Println(b) // true
	fmt.Println(c) // true
	fmt.Println(d) // false
	fmt.Println(e) // false
	fmt.Println(f) // false

}
