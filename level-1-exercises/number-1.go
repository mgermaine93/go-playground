// Using the short declaration operator, ASSIGN these VALUES to VARIABLES with the IDENTIFIERS "x", "y", and "z":  42, "James Bond", and true.

package main

import "fmt"

func main() {

	// Assign the values to variables
	x := 42
	y := "James Bond"
	z := true

	// Single print statement
	fmt.Println(x, y, z)

	// Multiple print statements
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)

}

